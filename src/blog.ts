export const blogPosts = { ru, en }

export function getPosts(lang: 'ru' | 'en'): BlogPost[] {
  return [...blogPosts[lang]].sort((a, b) => b.date.localeCompare(a.date))
}

export function getPost(lang: 'ru' | 'en', slug: string): BlogPost | undefined {
  return blogPosts[lang].find((p) => p.slug === slug)
}
